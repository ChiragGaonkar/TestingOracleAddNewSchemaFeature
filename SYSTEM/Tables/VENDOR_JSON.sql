CREATE TABLE "SYSTEM".vendor_json (
  vendor_poid NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY (MAXVALUE 9999999999 NOCACHE),
  vendor_acctno VARCHAR2(10 CHAR) NOT NULL,
  vendor_data BLOB DEFAULT empty_blob() CONSTRAINT vendor_data_json CHECK (vendor_data is json),
  vendor_id VARCHAR2(4 BYTE) GENERATED ALWAYS AS (JSON_VALUE("VENDOR_DATA" FORMAT JSON , '$.vendor.id' RETURNING VARCHAR2(4) NULL ON ERROR)),
  extid VARCHAR2(16 BYTE) GENERATED ALWAYS AS (JSON_VALUE("VENDOR_DATA" FORMAT JSON , '$.vendor.externalid' RETURNING VARCHAR2(16) NULL ON ERROR)),
  "SUCCESS" VARCHAR2(5 BYTE) GENERATED ALWAYS AS (JSON_VALUE("VENDOR_DATA" FORMAT JSON , '$.success' RETURNING VARCHAR2(5) NULL ON ERROR)),
  eft VARCHAR2(5 BYTE) GENERATED ALWAYS AS (JSON_VALUE("VENDOR_DATA" FORMAT JSON , '$.vendor.custentity_2663_payment_method' RETURNING VARCHAR2(5) NULL ON ERROR)),
  "REALM" VARCHAR2(15 CHAR),
  date_created TIMESTAMP GENERATED ALWAYS AS (JSON_VALUE("VENDOR_DATA" FORMAT JSON , '$.vendor.datecreated' RETURNING TIMESTAMP NULL ON ERROR)),
  last_modified TIMESTAMP GENERATED ALWAYS AS (JSON_VALUE("VENDOR_DATA" FORMAT JSON , '$.vendor.lastmodifieddate' RETURNING TIMESTAMP NULL ON ERROR)),
  vendor_acctcode VARCHAR2(10 CHAR) NOT NULL,
  CONSTRAINT vendor_json__pk PRIMARY KEY (vendor_poid)
);